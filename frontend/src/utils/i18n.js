export function getSavedLanguage() {
  try {
    const saved = localStorage.getItem('toolx.lang')
    if (saved === 'en' || saved === 'vi') return saved
  } catch {}
  return 'en'
}

export function saveLanguage(lang) {
  try { localStorage.setItem('toolx.lang', lang) } catch {}
}